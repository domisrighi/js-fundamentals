// Comparações retornam true ou false, e podemos comparar utilizando (===):

const primeiroNum = 3;
const segundoNum = 5;

console.log(primeiroNum === segundoNum);

// Comparar strings:

const frutaUm = "morango";
const frutaDois = "morango";

console.log(frutaUm === frutaDois);

// Comparando boolean com número:

const bacharel = false;

console.log(bacharel === 1);